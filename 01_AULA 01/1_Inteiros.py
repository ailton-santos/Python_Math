# Limpa a área de console para facilitar a visualização do resultado.
print ('\n' * 100)

# Define valores. Somente inteiros.
a = 5
b = 2

# Operações básicas.

soma = a + b
subtracao = a - b
multiplicacao = a * b
divisao = a / b

print ('Resultado das operações entre a e b.')
print ('Como a e b são definidos como valores inteiros, resultados são somente\
                                                            números inteiros.')
print ('a =', a)
print ('b =', b)
print ('a + b =', soma)
print ('a - b =', subtracao)
print ('a * b =', multiplicacao)
print ('a / b =', divisao)
